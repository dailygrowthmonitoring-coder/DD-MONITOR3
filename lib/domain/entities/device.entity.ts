/**
 * Extracts a short, human-readable site code from a DD appliance hostname.
 *
 * Pattern: The DD series model number (DD followed by digits) is immediately
 * followed by an uppercase site code.
 *
 * Examples:
 *   "DD6300BSR.iq.zain.com"  → "BSR"
 *   "DD6300BGH.iq.zain.com"  → "BGH"
 *   "DD990KBSR.iq.zain.com"  → "BSR"
 *
 * Falls back to the first FQDN segment if the pattern is not matched.
 *
 * @param hostname - The FQDN hostname of the appliance.
 * @returns A short site-code string.
 */
export function deriveShortName(hostname: string): string {
  // Match DD followed by digits, then one or more uppercase letters (site code).
  const match = /DD\d+([A-Z]+)/i.exec(hostname);
  if (match !== null) {
    const siteCode = match[1];
    if (siteCode !== undefined && siteCode.length > 0) {
      return siteCode.toUpperCase();
    }
  }
  // Fallback: first label of the FQDN (e.g. "DD6300BSR" from "DD6300BSR.iq.zain.com")
  const firstLabel = hostname.split('.')[0];
  return firstLabel ?? hostname;
}
