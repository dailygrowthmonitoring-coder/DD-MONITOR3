/**
 * Data required to create a new Device record.
 * id, snapshot fields, and timestamps are generated by the system.
 */
export interface CreateDevice {
  readonly hostname: string;
  readonly shortName: string;
  readonly model?: string;
  readonly serialNumber?: string;
  readonly chassisSerial?: string;
  readonly location?: string;
  readonly osVersion?: string;
  readonly totalCapacity?: Gib;
}

/**
 * Partial update applied to an existing Device.
 * Only the fields listed here can change after creation.
 */
export interface UpdateDevice {
  readonly model?: string;
  readonly serialNumber?: string;
  readonly chassisSerial?: string;
  readonly location?: string;
  readonly osVersion?: string;
  readonly totalCapacity?: Gib;
  readonly isActive?: boolean;
  /** Updated by DeviceRepository after ingest (snapshot refresh). */
  readonly lastReportDate?: Date;
  /** Updated by DeviceRepository after ingest (snapshot refresh). */
  readonly lastSeenAt?: Date;
  /** Updated by DeviceRepository after ingest (snapshot refresh). */
  readonly lastUsedPercent?: StoragePercent;
  /** Updated by AlertEngine via DeviceRepository after rule evaluation. */
  readonly lastStatus?: DeviceStatus;
  /** Updated by AlertEngine via DeviceRepository after rule evaluation. */
  readonly lastActiveAlerts?: number;
}

