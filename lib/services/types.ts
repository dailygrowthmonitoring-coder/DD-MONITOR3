/**
 * Layer 5 — Service layer shared DTOs and output types.
 *
 * These types are the contract between services (Layer 5) and API routes
 * (Layer 8). They must not import from app/ or any outer layer.
 *
 * Domain entity types (Device, Report, Alert, etc.) are defined in
 * lib/domain and used directly — no duplication here.
 */

import type { DeviceStatus } from '@/lib/domain';
import type { CreateAlert } from '@/lib/domain';

// ---------------------------------------------------------------------------
// Ingestion pipeline output
// ---------------------------------------------------------------------------

/**
 * The result returned by IngestionService.ingest() when ingestion succeeds.
 * Consumed by the API route to construct its 200 response body.
 */
export interface IngestionOutcome {
  /** UUID of the device this report belongs to. */
  readonly deviceId: string;

  /** UUID of the upserted dd_reports row. */
  readonly reportId: string;

  /** Fully-qualified hostname of the appliance, e.g. "DD6300BSR.iq.zain.com". */
  readonly hostname: string;

  /** ISO date the report covers, e.g. "2026-06-10". */
  readonly reportDate: string;

  /** True when this was the first report ever received from this appliance. */
  readonly isNewDevice: boolean;

  /** True when the exact same raw file (same SHA-256 hash) was already ingested. */
  readonly isReprocessed: boolean;

  /** Number of enabled alert rules evaluated against the report. */
  readonly alertsEvaluated: number;

  /** Number of rule-engine alerts that fired (threshold conditions met). */
  readonly alertsFired: number;

  /** Number of alerts extracted directly from the autosupport file. */
  readonly applianceAlertCount: number;

  /** Overall health status derived by the alert engine. */
  readonly deviceStatus: DeviceStatus;

  /** Non-fatal parse errors encountered; isValid may still be true. */
  readonly parseErrors: readonly string[];

  /** Wall-clock duration of the entire ingest pipeline in milliseconds. */
  readonly durationMs: number;
}

// ---------------------------------------------------------------------------
// Alert engine output
// ---------------------------------------------------------------------------

/**
 * Result of AlertEngineService.evaluate().
 * Contains rule-generated alerts and the derived DeviceStatus.
 */
export interface AlertEvaluationResult {
  /** Overall health status, derived from worst active-alert severity. */
  readonly deviceStatus: DeviceStatus;

  /**
   * Alerts generated by the rule engine.
   * These have source = AlertSource.RuleEngine and no appliance alert_id.
   * Note: reportId is a placeholder here; the ingestion service fills in the
   * real UUID after the report has been upserted.
   */
  readonly engineAlerts: readonly CreateAlert[];

  /** Total number of enabled rules evaluated. */
  readonly rulesEvaluated: number;

  /** Number of rules whose threshold condition was satisfied. */
  readonly rulesFired: number;

  /**
   * True when at least one CRITICAL or WARNING engine alert fired.
   * Used by the ingestion service to decide whether to request a notification.
   */
  readonly notificationRequired: boolean;
}

// ---------------------------------------------------------------------------
// Chart trend data
// ---------------------------------------------------------------------------

/** One data point in a device's storage utilisation trend chart. */
export interface StorageTrendPoint {
  /** ISO date string, e.g. "2026-06-10". */
  readonly date: string;
  readonly totalGib: number;
  readonly usedGib: number;
  readonly usedPercent: number;
  /** Null when the report did not include cleanable space data. */
  readonly cleanableGib: number | null;
}

/** One data point in a device's compression trend chart. */
export interface CompressionTrendPoint {
  readonly date: string;
  /** Overall compression factor (all-time cumulative). Null if not reported. */
  readonly totalFactor: number | null;
  /** Storage reduction percentage. Null if not reported. */
  readonly reductionPercent: number | null;
  /** Last-7-days total compression factor. Null if not reported. */
  readonly last7DaysFactor: number | null;
}

// ---------------------------------------------------------------------------
// Runway estimate
// ---------------------------------------------------------------------------

/**
 * Storage runway projection for one device, based on linear regression of
 * recent daily growth. Answers "when will this device fill up?"
 */
export interface RunwayEstimate {
  readonly deviceId: string;
  readonly hostname: string;

  /** Most recent storage_used_percent (0–100). */
  readonly currentUsedPercent: number;

  /** Average daily growth in GiB derived from the regression slope. */
  readonly avgDailyGrowthGib: number;

  /**
   * Estimated days until usedGib reaches totalGib.
   * Null when avgDailyGrowthGib ≤ 0 (not growing or shrinking).
   */
  readonly estimatedDaysRemaining: number | null;

  /** ISO date of projected fill date. Null when not growing. */
  readonly projectedFillDate: string | null;

  /** Number of daily reports used for the regression. */
  readonly dataPointsUsed: number;

  readonly totalCapacityGib: number | null;
  readonly usedGib: number | null;
}

