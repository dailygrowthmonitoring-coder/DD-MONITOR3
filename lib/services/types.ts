export interface DeviceComparisonRow {
  readonly deviceId: string;
  readonly hostname: string;
  readonly shortName: string;

  /** ISO date of the most recent report. Null if no reports yet. */
  readonly reportDate: string | null;

  readonly totalGib: number | null;
  readonly usedGib: number | null;
  readonly usedPercent: number | null;

  /** All-time cumulative compression factor. Null if not reported. */
  readonly totalFactor: number | null;

  readonly deviceStatus: DeviceStatus;
  readonly failedDisks: number;
  readonly activeAlerts: number;
}

// ---------------------------------------------------------------------------
// Fleet-wide analytics types (used by analytics routes)
// ---------------------------------------------------------------------------

/**
 * One row in the fleet storage trend API response.
 * Each row represents one device on one day. Clients group by date to plot
 * per-device trends or aggregate across devices.
 */
export interface FleetStorageTrendRow {
  readonly deviceId: string;
  readonly hostname: string;
  /** ISO date string, e.g. "2026-06-10". */
  readonly date: string;
  readonly totalGib: number;
  readonly usedGib: number;
  readonly usedPercent: number;
}

/**
 * One row in the fleet daily summary API response.
 * Aggregates device counts and storage across the whole fleet for a given day.
 * Days with no reports from any device are omitted.
 */
export interface FleetDailySummaryRow {
  /** ISO date string, e.g. "2026-06-10". */
  readonly date: string;
  readonly totalDevices: number;
  readonly healthyCount: number;
  readonly warningCount: number;
  readonly criticalCount: number;
  readonly unknownCount: number;
  /** Sum of totalGib across all reporting devices. */
  readonly totalCapacityGib: number;
  /** Sum of usedGib across all reporting devices. */
  readonly totalUsedGib: number;
  /** Sum of activeTotalAlerts across all reporting devices. */
  readonly totalActiveAlerts: number;
}

/**
 * One row in the alert trend API response.
 * Shows alert counts per severity for a single day.
 * Days with zero alerts are included so charts don't have gaps.
 */
export interface AlertTrendRow {
  /** ISO date string, e.g. "2026-06-10". */
  readonly date: string;
  readonly criticalCount: number;
  readonly warningCount: number;
  readonly infoCount: number;
}

/**
 * Fleet-wide health summary used by the System Health page stat cards.
 */
export interface FleetHealthSummary {
  readonly totalDevices: number;
  readonly healthyCount: number;
  readonly warningCount: number;
  readonly criticalCount: number;
  readonly unknownCount: number;
  /** Sum of totalGib across all active devices (from last snapshot). */
  readonly totalCapacityGib: number;
  /** Sum of usedGib across all active devices (from last snapshot). */
  readonly totalUsedGib: number;
  /** Fleet-wide average storage utilisation percent (0–100). */
  readonly avgUsedPercent: number;
  /** Total active alerts across the fleet right now. */
  readonly totalActiveAlerts: number;
}

/**
 * Per-device ingestion pipeline status.
 * Used by the System Health page ingestion status table.
 */
export interface IngestionStatusPerDevice {
  readonly deviceId: string;
  readonly hostname: string;
  readonly shortName: string;
  /** ISO date of the last ingested report. Null if never ingested. */
  readonly lastReportDate: string | null;
  /** Hours since the last report was received. Null if never ingested. */
  readonly lastReportAgeHours: number | null;
  /**
   * True when no report has been received for today and the current UTC hour
   * is past config.REPORT_DEADLINE_HOUR.
   */
  readonly isOverdue: boolean;
  readonly lastStatus: DeviceStatus;
}

